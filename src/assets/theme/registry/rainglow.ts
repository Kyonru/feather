export const rainglowThemeIds: RainglowThemeId[] = rainglowThemeSeeds.map((theme) => theme.id);
export const rainglowLightThemeIds: RainglowThemeId[] = rainglowThemeSeeds
  .filter((theme) => theme.kind === 'light')
  .map((theme) => theme.id);
export const rainglowDarkThemeIds: RainglowThemeId[] = rainglowThemeSeeds
  .filter((theme) => theme.kind === 'dark')
  .map((theme) => theme.id);
export const rainglowContrastThemeIds: RainglowThemeId[] = rainglowThemeSeeds
  .filter((theme) => theme.kind === 'contrast')
  .map((theme) => theme.id);

export const rainglowThemes = Object.fromEntries(
  rainglowThemeSeeds.map((theme) => {
    const seed = createRainglowSeed(theme);

    return [
      theme.id,
      {
        id: theme.id,
        label: seed.label,
        family: 'Rainglow',
        mode: seed.mode,
        swatches: seed.swatches,
        variables: seed.variables,
        syntax: createSyntaxHighlighterStyle(seed),
      },
    ];
  }),
) as Record<RainglowThemeId, AppTheme>;
