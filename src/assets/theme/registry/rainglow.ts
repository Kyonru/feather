import { createSyntaxHighlighterStyle, type AppTheme, type RainglowThemeId, type ThemeMode, type ThemeSeed } from './types';

type RainglowThemeKind = 'light' | 'dark' | 'contrast';

type RainglowSeed = {
  id: RainglowThemeId;
  label: string;
  mode: ThemeMode;
  kind: RainglowThemeKind;
  swatches: string[];
  background: string;
  foreground: string;
  card: string;
  popover: string;
  primary: string;
  primaryForeground: string;
  secondary: string;
  mutedForeground: string;
  accent: string;
  destructive: string;
  border: string;
  input: string;
  ring: string;
  success: string;
  warning: string;
  purple: string;
  syntax: ThemeSeed['syntax'];
};

function createRainglowSeed(theme: RainglowSeed): ThemeSeed {
  return {
    label: theme.label,
    mode: theme.mode,
    swatches: theme.swatches,
    variables: {
      background: theme.background,
      foreground: theme.foreground,
      card: theme.card,
      'card-foreground': theme.foreground,
      popover: theme.popover,
      'popover-foreground': theme.foreground,
      primary: theme.primary,
      'primary-foreground': theme.primaryForeground,
      secondary: theme.secondary,
      'secondary-foreground': theme.foreground,
      muted: theme.secondary,
      'muted-foreground': theme.mutedForeground,
      accent: theme.accent,
      'accent-foreground': theme.mode === 'light' ? theme.primary : theme.foreground,
      destructive: theme.destructive,
      border: theme.border,
      input: theme.input,
      ring: theme.ring,
      'chart-1': theme.primary,
      'chart-2': theme.success,
      'chart-3': theme.warning,
      'chart-4': theme.purple,
      'chart-5': theme.destructive,
      sidebar: theme.card,
      'sidebar-foreground': theme.foreground,
      'sidebar-primary': theme.primary,
      'sidebar-primary-foreground': theme.primaryForeground,
      'sidebar-accent': theme.secondary,
      'sidebar-accent-foreground': theme.foreground,
      'sidebar-border': theme.border,
      'sidebar-ring': theme.ring,
      'plugin-accent': theme.purple,
      'plugin-active': theme.accent,
      'plugin-active-foreground': theme.foreground,
      'plugin-active-icon': theme.primary,
      'plugin-active-border': theme.ring,
    },
    syntax: theme.syntax,
  };
}

