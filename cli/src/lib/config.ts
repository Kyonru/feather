function renderConfig(config: Record<string, unknown>, context?: ConfigTemplateContext): string {
  const rows = Object.entries(config).map(([key, value]) => `  ${luaKey(key)} = ${luaValue(value, 2)},`);
  return `${managedHeader(context)}
return {
${rows.join("\n")}
${optionReference()}
}
`;
}

export function configTemplate(config?: Record<string, unknown>, context?: ConfigTemplateContext): string {
  if (config && Object.keys(config).length > 0) {
    return renderConfig(config, context);
  }

  return `${managedHeader(context)}
return {
  sessionName = "My Game",
${optionReference()}
}
`;
}
